// CG 자산 manifest 생성기
// public/images/events/ 아래에 실제로 존재하는 .png 파일 목록을 스캔해서
// src/cg-manifest.generated.ts 로 export.
//
// GameScreen.tsx의 CG cascade가 자산 부재 케이스에서 8개 후보를 직렬 404로
// 두드리는 비용을 없애기 위함. manifest lookup으로 hasCg를 즉시 결정.
//
// 실행: cd game && node scripts/generate-cg-manifest.mjs
// 자동 실행: package.json의 predev / prebuild 훅

import { readdirSync, statSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const EVENTS_DIR = join(ROOT, 'public/images/events');
const OUT_PATH = join(ROOT, 'src/cg-manifest.generated.ts');

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    // 백업/리뷰 디렉토리 제외 (_archive, _review 등 underscore prefix)
    if (name.startsWith('_')) continue;
    const full = join(dir, name);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      out.push(...walk(full));
    } else if (name.endsWith('.png')) {
      out.push(relative(EVENTS_DIR, full).replace(/\\/g, '/'));
    }
  }
  return out;
}

if (!existsSync(EVENTS_DIR)) {
  console.warn(`[cg-manifest] events dir not found, writing empty manifest: ${EVENTS_DIR}`);
}

const files = existsSync(EVENTS_DIR) ? walk(EVENTS_DIR).sort() : [];

const content = `// Auto-generated by scripts/generate-cg-manifest.mjs — do not edit.
// Lists every CG file present under public/images/events/ at build time.
// Used by GameScreen to skip non-existent CG candidates without 404 round-trips.
export const CG_MANIFEST: ReadonlySet<string> = new Set([
${files.map(f => `  '${f}',`).join('\n')}
]);
`;

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, content);
console.log(`[cg-manifest] ${files.length} files indexed → ${relative(ROOT, OUT_PATH)}`);
